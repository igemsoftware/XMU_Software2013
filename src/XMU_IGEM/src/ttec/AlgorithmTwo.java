    private static int min(int a, int b, int c)
    {
        int minimum;
        if (a <= b)
        {
            if (a <= c)
                minimum = a;
            else
                minimum = c;
        }
        else if (b >= c)
            minimum = c;
        else
            minimum = b;
        return minimum;
    }
